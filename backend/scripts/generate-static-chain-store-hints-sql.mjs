function buildRows(target = 10000) {
  const seen = new Set();
  /** @type {{ vn: string; cat: string; w: number }[]} */
  const out = [];

  function tryAdd(rawKey, cat, w) {
    const vn = normalizeVendorName(rawKey);
    if (!vn || vn.length < 3) return false;
    if (seen.has(vn)) return false;
    seen.add(vn);
    out.push({ vn, cat, w: Number(w) || 0 });
    return out.length >= target;
  }

  /** 正式な都道府県名＋店舗接尾辞（短縮しない） */
  const uniqSuffixes = [];
  const sufSeen = new Set();
  for (const s of [...PREFECTURES, ...EXTRA_SUFFIXES]) {
    const t = String(s ?? "").trim();
    if (!t || sufSeen.has(t)) continue;
    sufSeen.add(t);
    uniqSuffixes.push(t);
  }

  /** @type {{ base: string; cat: string; w: number }[]} */
  const roots = [];
  const rootSeen = new Set();
  for (const r of CHAIN_ROOTS) {
    const k = `${r.base}|${r.cat}`;
    if (rootSeen.has(k)) continue;
    if (normalizeVendorName(r.base).length < 4) continue;
    rootSeen.add(k);
    roots.push({ base: r.base, cat: r.cat, w: r.w ?? 0 });
  }

  // シャッフルしてカテゴリ偏りを緩和（決定的シード）
  let seed = 2166136261;
  function rnd() {
    seed = Math.imul(seed ^ (seed >>> 15), seed | 1);
    seed ^= seed + Math.imul(seed ^ (seed >>> 7), seed | 61);
    return ((seed ^ (seed >>> 14)) >>> 0) / 4294967296;
  }
  for (let i = roots.length - 1; i > 0; i -= 1) {
    const j = Math.floor(rnd() * (i + 1));
    [roots[i], roots[j]] = [roots[j], roots[i]];
  }

  outer: for (const { base, cat, w } of roots) {
    if (tryAdd(base, cat, w)) break outer;
    for (const suf of uniqSuffixes) {
      if (!suf) continue;
      if (tryAdd(`${base}${suf}`, cat, w)) break outer;
    }
  }

  return out;
}

